package com.krawler.spring.hrms.payroll.bank.tabularreport;

/**
 *
 * @author krawler
 */
public class Column<E> {

    private String name;
    private int length=20;

    public Column(String name) {
        this.name = name;
    }

    public Column(String name, int length) {
        this.name = name;
        this.length = length;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean isValidData(E data) {
        return String.valueOf(data).length()<=length;
    }
}
